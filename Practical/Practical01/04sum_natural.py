"""
The sum of the first n natural numbers is given by the formula:
Sum = n * (n + 1) / 2
For n = 10:
Sum = 10 * (10 + 1) / 2 = 55
"""

# This script calculates the sum of the first 10 natural numbers
sum_natural = sum(range(1, 11))  # Using sum() function with range()
print(f"Sum of the first 10 natural numbers is {sum_natural}")  # Printing the result
